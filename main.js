const container = document.getElementById('movies-wrapper');

movies.forEach((result, idx) => {

    // Create card element
    const card = document.createElement('div');
    card.classList = 'card';

    // Construct card content
    const content = `
    <div class="card" id="card-${idx}">
        <a href="#" onclick="clickHandler(${idx})">
            <img src=${result.image} alt="">
            <div class="title">${result.title}</div>
        </a>
    </div>
    `;

    // Append newyly created card element to the container
    container.innerHTML += content;
})

let modal = document.getElementById("myModal");

// Get the button that opens the modal

// Get the <span> element that closes the modal
let span = document.getElementsByClassName("close")[0];

// When the user clicks the button, open the modal 
let clickHandler = (idx) => {
    modal.style.display = "block";
    let movie = movies[idx];
    document.getElementById("m_image").src = movie.image;
    document.getElementById("m_desc").innerHTML = movie.desc;
    document.getElementById("m_judul").innerHTML = movie.title + " (" + movie.year + ")";


}

// When the user clicks on <span> (x), close the modal
span.onclick = function() {
    modal.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
    if (event.target == modal) {
        modal.style.display = "none";
    }
}